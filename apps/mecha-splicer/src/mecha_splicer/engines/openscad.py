def _rotary_joint_block_lines(j: RotaryJointSpec, *, with_header: bool) -> list[str]:
    b = get_bearing_dims(j.bearing)
    pocket_d = b.od_mm + 2 * j.clearance_mm
    pocket_h = b.width_mm + j.clearance_mm

    # Ensure a minimum block thickness around the bearing.
    w = max(j.block_w_mm, pocket_d + 2 * j.wall_mm)
    d = max(j.block_d_mm, pocket_d + 2 * j.wall_mm)
    h = max(j.block_h_mm, pocket_h + 2.0)

    hx = j.mount_hole_spacing_x_mm / 2.0
    hy = j.mount_hole_spacing_y_mm / 2.0

    lines: list[str] = []
    if with_header:
        lines.append(_header(f"rotary_joint_block name={j.name} bearing={j.bearing}"))
    lines.append(f"w = {w}; d = {d}; h = {h};")
    lines.append(f"pocket_d = {pocket_d}; pocket_h = {pocket_h};")
    lines.append(f"hole_d = {j.mount_hole_d_mm};")
    lines.append(f"hx = {hx}; hy = {hy};")
    lines.append("difference() {")
    lines.append("  cube([w, d, h], center=false);")
    # Bearing pocket (top-down)
    lines.append("  translate([w/2, d/2, h-pocket_h]) cylinder(h=pocket_h+1, d=pocket_d, center=false);")
    # Through shaft hole
    lines.append(f"  translate([w/2, d/2, -1]) cylinder(h=h+2, d={b.id_mm + 2 * j.clearance_mm}, center=false);")
    # Mounting holes (4-corner pattern)
    for sx in (-1, 1):
        for sy in (-1, 1):
            lines.append(f"  translate([w/2 + {sx}*hx, d/2 + {sy}*hy, -1]) cylinder(h=h+2, d=hole_d, center=false);")
    lines.append("}")
    return lines


