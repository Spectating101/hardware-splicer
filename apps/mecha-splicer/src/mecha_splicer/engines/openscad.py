def _belt_reduction_plate_lines(s: BeltReductionSpec) -> list[str]:
    hole_spacing = NEMA17.hole_spacing_mm
    bore_d = NEMA17.bore_d_mm
    b = get_bearing_dims("625zz")
    bearing_pocket_d = b.od_mm + 2 * s.clearance_mm
    shaft_hole_d = b.id_mm + 2 * s.clearance_mm

    # Place motor near left; driven shaft to the right; idler optionally above/below.
    mx = 28.0
    my = s.plate_h_mm / 2.0
    dx = mx + s.center_distance_mm
    dy = my
    ix = mx + s.center_distance_mm / 2.0
    iy = my + (s.idler_offset_mm if s.include_idler else 0.0)

    lines: list[str] = []
    lines.append(f"w={s.plate_w_mm}; h={s.plate_h_mm}; t={s.plate_t_mm};")
    lines.append("difference() {")
    lines.append("  cube([w, h, t], center=false);")
    # Motor bore + holes
    lines.append(f"  translate([{mx}, {my}, -1]) cylinder(h=t+2, d={bore_d}, center=false);")
    for sx in (-1, 1):
        for sy in (-1, 1):
            lines.append(
                f"  translate([{mx} + {sx}*{hole_spacing}/2, {my} + {sy}*{hole_spacing}/2, -1]) cylinder(h=t+2, d={NEMA17.mount_hole_d_mm}, center=false);"
            )
    # Driven shaft bearing pocket + through hole
    lines.append(f"  translate([{dx}, {dy}, t-({b.width_mm + s.clearance_mm})]) cylinder(h={b.width_mm + s.clearance_mm + 1}, d={bearing_pocket_d}, center=false);")
    lines.append(f"  translate([{dx}, {dy}, -1]) cylinder(h=t+2, d={shaft_hole_d}, center=false);")
    # Idler pocket if enabled
    if s.include_idler:
        lines.append(f"  translate([{ix}, {iy}, t-({b.width_mm + s.clearance_mm})]) cylinder(h={b.width_mm + s.clearance_mm + 1}, d={bearing_pocket_d}, center=false);")
        lines.append(f"  translate([{ix}, {iy}, -1]) cylinder(h=t+2, d={shaft_hole_d}, center=false);")
    lines.append("}")
    return lines


