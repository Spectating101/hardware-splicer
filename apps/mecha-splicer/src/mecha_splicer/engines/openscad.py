def _leadscrew_motor_mount_lines(a: LeadScrewAxisSpec) -> list[str]:
    hole_spacing = NEMA17.hole_spacing_mm
    bore_d = NEMA17.bore_d_mm
    plate_w = max(70.0, a.rod_spacing_mm + 35.0)
    plate_h = 80.0
    t = a.wall_mm * 2.0
    coupler_clear_d = 20.0

    lines: list[str] = []
    lines.append(f"plate_w = {plate_w};")
    lines.append(f"plate_h = {plate_h};")
    lines.append(f"t = {t};")
    lines.append(f"hole_spacing = {hole_spacing};")
    lines.append(f"bore_d = {bore_d};")
    lines.append(f"coupler_d = {coupler_clear_d};")
    lines.append("difference() {")
    lines.append("  cube([plate_w, plate_h, t], center=false);")
    lines.append("  translate([plate_w/2, plate_h*0.6, -1]) cylinder(h=t+2, d=bore_d, center=false);")
    for sx in (-1, 1):
        for sy in (-1, 1):
            lines.append(f"  translate([plate_w/2 + {sx}*hole_spacing/2, plate_h*0.6 + {sy}*hole_spacing/2, -1]) cylinder(h=t+2, d={NEMA17.mount_hole_d_mm}, center=false);")
    # Coupler clearance
    lines.append("  translate([plate_w/2, plate_h*0.3, -1]) cylinder(h=t+2, d=coupler_d, center=false);")
    # Rod clearances
    clamp_d = a.rod_d_mm + 2.0
    y = 12.0
    lines.append(f"  translate([plate_w/2 - {a.rod_spacing_mm}/2, {y}, -1]) cylinder(h=t+2, d={clamp_d}, center=false);")
    lines.append(f"  translate([plate_w/2 + {a.rod_spacing_mm}/2, {y}, -1]) cylinder(h=t+2, d={clamp_d}, center=false);")
    # Leadscrew clearance
    lines.append(f"  translate([plate_w/2, {y}, -1]) cylinder(h=t+2, d={a.screw_d_mm + 2.0}, center=false);")
    lines.append("}")
    return lines


def _leadscrew_end_support_lines(a: LeadScrewAxisSpec) -> list[str]:
    w = max(70.0, a.rod_spacing_mm + 35.0)
    h = 70.0
    t = a.wall_mm * 2.0
    bearing_d = 22.2
    bearing_t = 7.2

    lines: list[str] = []
    lines.append(f"w = {w}; h = {h}; t = {t};")
    lines.append("difference(){")
    lines.append(f"  cube([w, h, t], center=false);")
    # Bearing pocket
    lines.append(f"  translate([w/2, h*0.6, t - {bearing_t}]) cylinder(h={bearing_t+1}, d={bearing_d}, center=false);")
    # Leadscrew pass-through
    lines.append(f"  translate([w/2, h*0.6, -1]) cylinder(h={t+2}, d={a.screw_d_mm + 1.0}, center=false);")
    # Rod clearances
    clamp_d = a.rod_d_mm + 2.0
    y = h * 0.25
    lines.append(f"  translate([w/2 - {a.rod_spacing_mm}/2, {y}, -1]) cylinder(h={t+2}, d={clamp_d}, center=false);")
    lines.append(f"  translate([w/2 + {a.rod_spacing_mm}/2, {y}, -1]) cylinder(h={t+2}, d={clamp_d}, center=false);")
    lines.append("}")
    return lines


def _leadscrew_nut_carriage_lines(a: LeadScrewAxisSpec) -> list[str]:
    w = a.rod_spacing_mm + a.rod_d_mm + 26.0
    h = a.rod_d_mm + 26.0
    t = max(14.0, a.wall_mm * 4.0)
    rod_clear = a.rod_d_mm + a.clearance_mm * 2.0
    nut_w = 22.0
    nut_h = 22.0
    nut_t = 12.0

    lines: list[str] = []
    lines.append("difference(){")
    lines.append(f"  cube([{w}, {h}, {t}], center=false);")
    lines.append(f"  translate([{w/2 - a.rod_spacing_mm/2}, {h/2}, -1]) cylinder(h={t+2}, d={rod_clear}, center=false);")
    lines.append(f"  translate([{w/2 + a.rod_spacing_mm/2}, {h/2}, -1]) cylinder(h={t+2}, d={rod_clear}, center=false);")
    lines.append(f"  translate([{w/2 - nut_w/2}, {h/2 - nut_h/2}, {t/2 - nut_t/2}]) cube([{nut_w}, {nut_h}, {nut_t}], center=false);")
    lines.append(f"  translate([{w/2}, {h/2}, -1]) cylinder(h={t+2}, d={a.screw_d_mm + 2.0}, center=false);")
    lines.append("}")
    return lines


def _leadscrew_preview_lines(a: LeadScrewAxisSpec) -> list[str]:
    lines: list[str] = []
    lines.append(f"rod_d = {a.rod_d_mm};")
    lines.append(f"rod_spacing = {a.rod_spacing_mm};")
    lines.append(f"rod_len = {a.rod_length_mm};")
    lines.append(f"screw_d = {a.screw_d_mm};")
    lines.append(f"screw_len = {a.screw_length_mm};")
    lines.append("")
    lines.append("color([0.7,0.7,0.7]) {")
    lines.append("  translate([0, -rod_spacing/2, 0]) rotate([0,90,0]) cylinder(h=rod_len, d=rod_d, center=false);")
    lines.append("  translate([0,  rod_spacing/2, 0]) rotate([0,90,0]) cylinder(h=rod_len, d=rod_d, center=false);")
    lines.append("}")
    lines.append("color([0.2,0.2,0.2]) translate([0,0,0]) rotate([0,90,0]) cylinder(h=screw_len, d=screw_d, center=false);")
    if a.frame == "2020_extrusion":
        lines.append("color([0.3,0.3,0.3]) translate([0, -25, -10]) cube([rod_len, 20, 20], center=false);")
        lines.append("color([0.3,0.3,0.3]) translate([0,  5, -10]) cube([rod_len, 20, 20], center=false);")
    lines.append("// Import and position printable parts manually for full assembly.")
    return lines


