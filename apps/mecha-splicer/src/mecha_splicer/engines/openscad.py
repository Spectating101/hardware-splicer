def _header(comment: str) -> str:
    return "\n".join(["// Auto-generated by Mecha-Splicer", f"// {comment}", "", "$fn = 64;", ""])


def _wrap_module(module: str, body_lines: list[str], *, call: bool = True) -> str:
    lines: list[str] = []
    lines.append(f"module {module}() {{")
    for l in body_lines:
        lines.append(f"  {l}")
    lines.append("}")
    if call:
        lines.append(f"{module}();")
    return "\n".join(lines) + "\n"


def generate_enclosure_scad(enc: EnclosureSpec) -> str:
    outer_w = enc.inner_w_mm + 2 * enc.wall_mm
    outer_d = enc.inner_d_mm + 2 * enc.wall_mm
    outer_h = enc.inner_h_mm + enc.floor_mm

    lines: list[str] = []
    lines.append(_header(f"enclosure={enc.name}"))
    lines.append(f"inner_w = {enc.inner_w_mm};")
    lines.append(f"inner_d = {enc.inner_d_mm};")
    lines.append(f"inner_h = {enc.inner_h_mm};")
    lines.append(f"wall = {enc.wall_mm};")
    lines.append(f"floor = {enc.floor_mm};")
    lines.append(f"lid_t = {enc.lid_mm};")
    lines.append(f"standoff_h = {enc.standoff_h_mm};")
    lines.append(f"standoff_od = {enc.standoff_od_mm};")
    lines.append(f"standoff_hole_d = {enc.standoff_hole_d_mm};")
    lines.append("")

    lines.append("module base() {")
    lines.append("  union() {")
    lines.append("    difference() {")
    lines.append(f"      cube([{outer_w}, {outer_d}, {outer_h}], center=false);")
    lines.append(
        f"      translate([{enc.wall_mm}, {enc.wall_mm}, {enc.floor_mm}]) cube([{enc.inner_w_mm}, {enc.inner_d_mm}, {enc.inner_h_mm}], center=false);"
    )
    for c in enc.cutouts:
        lines.extend(_cutout_scad(c, enc))
    # Lid screw clearance holes through the lid seating plane (optional, simplified)
    if enc.include_lid_screw_holes and enc.mount_holes:
        for mh in enc.mount_holes:
            x = enc.wall_mm + mh.x_mm
            y = enc.wall_mm + mh.y_mm
            lines.append(f"      translate([{x}, {y}, -1]) cylinder(h={outer_h + 2}, d={enc.lid_screw_hole_d_mm}, center=false);")
    lines.append("    }")

    # Standoffs: cylinders with center hole
    if enc.include_standoffs and enc.mount_holes:
        lines.append("    // Standoffs")
        lines.append("    difference() {")
        lines.append("      union() {")
        for mh in enc.mount_holes:
            x = enc.wall_mm + mh.x_mm
            y = enc.wall_mm + mh.y_mm
            lines.append(f"        translate([{x}, {y}, {enc.floor_mm}]) cylinder(h=standoff_h, d=standoff_od, center=false);")
        lines.append("      }")
        for mh in enc.mount_holes:
            x = enc.wall_mm + mh.x_mm
            y = enc.wall_mm + mh.y_mm
            lines.append(f"      translate([{x}, {y}, {enc.floor_mm - 1}]) cylinder(h=standoff_h + 2, d=standoff_hole_d, center=false);")
        lines.append("    }")
    lines.append("  }")
    lines.append("}")
    lines.append("")

    lines.append("module lid() {")
    lines.append("  difference() {")
    lines.append(f"    cube([{outer_w}, {outer_d}, {enc.lid_mm}], center=false);")
    if enc.include_lid_screw_holes and enc.mount_holes:
        for mh in enc.mount_holes:
            x = enc.wall_mm + mh.x_mm
            y = enc.wall_mm + mh.y_mm
            lines.append(f"    translate([{x}, {y}, -1]) cylinder(h={enc.lid_mm + 2}, d={enc.lid_screw_hole_d_mm}, center=false);")
    lines.append("  }")
    lines.append("}")
    lines.append("")

    body: list[str] = []
    body.append("base();")
    if enc.lid_style == "screw":
        body.append(f"translate([0, 0, {outer_h + 1.0}]) lid();")

    lines.append(_wrap_module("enclosure", body))
    return "\n".join(lines) + "\n"


def _cutout_scad(c: Cutout, enc: EnclosureSpec) -> list[str]:
    lines: list[str] = []
    depth = enc.wall_mm + 1.0
    if c.kind == "rect" and c.rect:
        r = c.rect
        if c.face == "front":
            lines.append(f"    translate([{enc.wall_mm + r.x_mm}, 0, {enc.floor_mm + r.y_mm}]) cube([{r.w_mm}, {depth}, {r.h_mm}], center=false);")
        elif c.face == "back":
            outer_d = enc.inner_d_mm + 2 * enc.wall_mm
            lines.append(f"    translate([{enc.wall_mm + r.x_mm}, {outer_d - depth}, {enc.floor_mm + r.y_mm}]) cube([{r.w_mm}, {depth}, {r.h_mm}], center=false);")
        elif c.face == "left":
            lines.append(f"    translate([0, {enc.wall_mm + r.x_mm}, {enc.floor_mm + r.y_mm}]) cube([{depth}, {r.w_mm}, {r.h_mm}], center=false);")
        elif c.face == "right":
            outer_w = enc.inner_w_mm + 2 * enc.wall_mm
            lines.append(f"    translate([{outer_w - depth}, {enc.wall_mm + r.x_mm}, {enc.floor_mm + r.y_mm}]) cube([{depth}, {r.w_mm}, {r.h_mm}], center=false);")
        elif c.face == "top":
            outer_h = enc.inner_h_mm + enc.floor_mm
            lines.append(f"    translate([{enc.wall_mm + r.x_mm}, {enc.wall_mm + r.y_mm}, {outer_h - depth}]) cube([{r.w_mm}, {r.h_mm}, {depth}], center=false);")
    elif c.kind == "circle" and c.circle:
        cc = c.circle
        if c.face in ("front", "back"):
            y = 0 if c.face == "front" else enc.inner_d_mm + 2 * enc.wall_mm - depth
            lines.append(f"    translate([{enc.wall_mm + cc.x_mm}, {y}, {enc.floor_mm + cc.y_mm}]) rotate([90,0,0]) cylinder(h={depth}, d={cc.d_mm}, center=false);")
        elif c.face in ("left", "right"):
            x = 0 if c.face == "left" else enc.inner_w_mm + 2 * enc.wall_mm - depth
            lines.append(f"    translate([{x}, {enc.wall_mm + cc.x_mm}, {enc.floor_mm + cc.y_mm}]) rotate([0,90,0]) cylinder(h={depth}, d={cc.d_mm}, center=false);")
    return lines


def generate_bracket_scad(br: BracketSpec) -> str:
    lines: list[str] = []
    lines.append(_header(f"bracket={br.name}"))
    lines.append(f"w = {br.w_mm};")
    lines.append(f"d = {br.d_mm};")
    lines.append(f"t = {br.t_mm};")
    lines.append(f"hole_d = {br.hole_d_mm};")
    lines.append(f"hole_spacing = {br.hole_spacing_mm};")
    lines.append("")
    lines.append("difference() {")
    lines.append("  cube([w, d, t], center=false);")
    lines.append("  translate([(w-hole_spacing)/2, d/2, -1]) cylinder(h=t+2, d=hole_d, center=false);")
    lines.append("  translate([(w+hole_spacing)/2, d/2, -1]) cylinder(h=t+2, d=hole_d, center=false);")
    lines.append("}")
    header = _header(f"bracket={br.name}")
    body = []
    body.append(f"w = {br.w_mm}; d = {br.d_mm}; t = {br.t_mm};")
    body.append(f"hole_d = {br.hole_d_mm}; hole_spacing = {br.hole_spacing_mm};")
    body.append("difference() {")
    body.append("  cube([w, d, t], center=false);")
    body.append("  translate([(w-hole_spacing)/2, d/2, -1]) cylinder(h=t+2, d=hole_d, center=false);")
    body.append("  translate([(w+hole_spacing)/2, d/2, -1]) cylinder(h=t+2, d=hole_d, center=false);")
    body.append("}")
    return "\n".join([header, _wrap_module("bracket", body)])


def generate_servo_mount_scad(sm: ServoMountSpec) -> str:
    dims = get_servo_dims(sm.servo_type)

    lines: list[str] = []
    lines.append(_header(f"servo_mount={sm.name} type={dims.name}"))
    lines.append(f"plate_w = {sm.plate_w_mm};")
    lines.append(f"plate_h = {sm.plate_h_mm};")
    lines.append(f"plate_t = {sm.plate_t_mm};")
    lines.append(f"clearance = {sm.clearance_mm};")
    lines.append(f"hole_d = {sm.hole_d_mm};")
    lines.append(f"body_w = {dims.body_w_mm};")
    lines.append(f"body_h = {dims.body_h_mm};")
    lines.append(f"flange_w = {dims.flange_w_mm};")
    lines.append(f"flange_h = {dims.flange_h_mm};")
    lines.append(f"hole_spacing_x = {dims.flange_hole_spacing_x_mm};")
    lines.append("")

    # Plate with a rectangular cutout for servo body and two flange holes.
    lines.append("difference() {")
    lines.append("  cube([plate_w, plate_h, plate_t], center=false);")
    # Body cutout centered
    lines.append("  translate([(plate_w-body_w-clearance*2)/2, (plate_h-body_h-clearance*2)/2, -1])")
    lines.append("    cube([body_w+clearance*2, body_h+clearance*2, plate_t+2], center=false);")
    # Flange hole pair along X axis, centered on plate
    lines.append("  translate([plate_w/2 - hole_spacing_x/2, plate_h/2, -1]) cylinder(h=plate_t+2, d=hole_d, center=false);")
    lines.append("  translate([plate_w/2 + hole_spacing_x/2, plate_h/2, -1]) cylinder(h=plate_t+2, d=hole_d, center=false);")
    lines.append("}")

    header = _header(f"servo_mount={sm.name} servo={sm.servo_type}")
    body = lines[len(header.splitlines()):]  # crude, but lines currently start with header
    # Rebuild body directly to avoid relying on the header length.
    d = get_servo_dims(sm.servo_type)
    clearance = sm.clearance_mm
    plate_w = sm.plate_w_mm
    plate_h = sm.plate_h_mm
    plate_t = sm.plate_t_mm
    hole_d = sm.hole_d_mm
    body_w = d.body_w_mm
    body_h = d.body_h_mm
    flange_w = d.flange_w_mm
    flange_h = d.flange_h_mm
    hole_spacing_x = d.flange_hole_spacing_x_mm

    body_lines: list[str] = []
    body_lines.append(f"plate_w = {plate_w}; plate_h = {plate_h}; plate_t = {plate_t};")
    body_lines.append(f"clearance = {clearance}; hole_d = {hole_d};")
    body_lines.append(f"body_w = {body_w}; body_h = {body_h}; flange_w = {flange_w}; flange_h = {flange_h};")
    body_lines.append(f"hole_spacing_x = {hole_spacing_x};")
    body_lines.append("difference() {")
    body_lines.append("  cube([plate_w, plate_h, plate_t], center=false);")
    body_lines.append("  translate([(plate_w-body_w-clearance*2)/2, (plate_h-body_h-clearance*2)/2, -1])")
    body_lines.append("    cube([body_w+clearance*2, body_h+clearance*2, plate_t+2], center=false);")
    body_lines.append("  translate([plate_w/2 - hole_spacing_x/2, plate_h/2, -1]) cylinder(h=plate_t+2, d=hole_d, center=false);")
    body_lines.append("  translate([plate_w/2 + hole_spacing_x/2, plate_h/2, -1]) cylinder(h=plate_t+2, d=hole_d, center=false);")
    body_lines.append("}")
    return "\n".join([header, _wrap_module("servo_mount", body_lines)])


def generate_linear_axis_parts_scad(axis: LinearAxisSpec) -> dict[str, str]:
    """
    Generate separate OpenSCAD part files for a belt-driven linear axis:
      - motor_mount.scad
      - idler_mount.scad
      - carriage.scad
      - assembly_preview.scad (non-printable helper)
    """
    parts: dict[str, str] = {}
    parts["motor_mount.scad"] = _wrap_module("motor_mount", _linear_axis_motor_mount_lines(axis))
    parts["idler_mount.scad"] = _wrap_module("idler_mount", _linear_axis_idler_mount_lines(axis))
    parts["carriage.scad"] = _wrap_module("carriage", _linear_axis_carriage_lines(axis))
    parts["belt_clamp.scad"] = _wrap_module("belt_clamp", _linear_axis_belt_clamp_lines(axis))
    if axis.include_rod_holders:
        parts["rod_holder.scad"] = _wrap_module("rod_holder", _linear_axis_rod_holder_lines(axis))
    if axis.include_tensioner:
        parts["tensioner_block.scad"] = _wrap_module("tensioner_block", _linear_axis_tensioner_lines(axis))
    if axis.include_endstops:
        parts["endstop_mount.scad"] = _wrap_module("endstop_mount", _linear_axis_endstop_mount_lines(axis))
    parts["assembly_preview.scad"] = _wrap_module("assembly_preview", _linear_axis_preview_lines(axis))
    return parts


def generate_leadscrew_axis_parts_scad(axis: LeadScrewAxisSpec) -> dict[str, str]:
    """
    Generate printable parts for a lead-screw axis:
      - motor_mount.scad (with coupler clearance)
      - screw_end_support.scad
      - carriage_nut_mount.scad
      - rod_holder.scad (reuse style)
      - endstop_mount.scad (reuse style)
      - assembly_preview.scad
    """
    parts: dict[str, str] = {}
    parts["ls_motor_mount.scad"] = _wrap_module("ls_motor_mount", _leadscrew_motor_mount_lines(axis))
    parts["ls_screw_end_support.scad"] = _wrap_module("ls_screw_end_support", _leadscrew_end_support_lines(axis))
    parts["ls_carriage_nut_mount.scad"] = _wrap_module("ls_carriage_nut_mount", _leadscrew_nut_carriage_lines(axis))
    if axis.include_rod_holders:
        # reuse the rod holder generator by mapping fields
        parts["rod_holder.scad"] = _wrap_module(
            "rod_holder",
            _linear_axis_rod_holder_lines(
            LinearAxisSpec.model_validate(
                {
                    "travel_mm": axis.travel_mm,
                    "rod_d_mm": axis.rod_d_mm,
                    "rod_spacing_mm": axis.rod_spacing_mm,
                    "rod_length_mm": axis.rod_length_mm,
                    "clearance_mm": axis.clearance_mm,
                    "wall_mm": axis.wall_mm,
                }
            )
            ),
        )
    if axis.include_endstops:
        parts["endstop_mount.scad"] = _wrap_module(
            "endstop_mount",
            _linear_axis_endstop_mount_lines(
            LinearAxisSpec.model_validate(
                {
                    "travel_mm": axis.travel_mm,
                    "rod_d_mm": axis.rod_d_mm,
                    "rod_spacing_mm": axis.rod_spacing_mm,
                    "rod_length_mm": axis.rod_length_mm,
                    "clearance_mm": axis.clearance_mm,
                    "wall_mm": axis.wall_mm,
                }
            )
            ),
        )
    parts["ls_assembly_preview.scad"] = _wrap_module("ls_assembly_preview", _leadscrew_preview_lines(axis))
    return parts


