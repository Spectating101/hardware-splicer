def _linear_axis_rod_holder(a: LinearAxisSpec) -> str:
    """
    Two-piece clamp block for holding a smooth rod to a frame/plate.
    This is intentionally simple and printable.
    """
    rod_d = a.rod_d_mm
    clear = a.clearance_mm
    od = rod_d + 10.0
    w = 24.0
    h = 18.0
    t = max(10.0, a.wall_mm * 3.0)
    slit = 1.5
    bolt_d = 3.2 if a.rod_holder_bolt == "m3" else 2.2
    nut_flat = 5.6 if a.rod_holder_bolt == "m3" else 4.0

    lines: list[str] = []
    lines.append(_header(f"linear_axis_rod_holder name={a.name}"))
    lines.append(f"rod_d = {rod_d};")
    lines.append(f"rod_clear = {rod_d + clear*2};")
    lines.append(f"od = {od};")
    lines.append(f"w = {w};")
    lines.append(f"h = {h};")
    lines.append(f"t = {t};")
    lines.append(f"slit = {slit};")
    lines.append(f"bolt_d = {bolt_d};")
    lines.append(f"nut_flat = {nut_flat};")
    lines.append("")

    # Base block with semi-circular bore and clamping slit.
    lines.append("difference() {")
    lines.append("  cube([w, od, t], center=false);")
    lines.append("  // rod bore")
    lines.append("  translate([w/2, od/2, -1]) cylinder(h=t+2, d=rod_clear, center=false);")
    lines.append("  // clamp slit")
    lines.append("  translate([w/2 - slit/2, 0, -1]) cube([slit, od, t+2], center=false);")
    lines.append("  // clamp bolt holes (2)")
    lines.append("  translate([w*0.25, od*0.25, -1]) cylinder(h=t+2, d=bolt_d, center=false);")
    lines.append("  translate([w*0.75, od*0.75, -1]) cylinder(h=t+2, d=bolt_d, center=false);")
    lines.append("  // hex nut pockets (underside)")
    lines.append("  translate([w*0.25, od*0.25, 0]) cylinder(h=4, d=nut_flat, $fn=6, center=false);")
    lines.append("  translate([w*0.75, od*0.75, 0]) cylinder(h=4, d=nut_flat, $fn=6, center=false);")
    lines.append("}")
    return "\n".join(lines) + "\n"
