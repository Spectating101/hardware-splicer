def _linear_axis_motor_mount(a: LinearAxisSpec) -> str:
    # NEMA17 face pattern constants (approx): 31mm hole spacing, 22mm center bore clearance.
    hole_spacing = 31.0
    bore_d = 22.0
    plate_w = max(60.0, a.rod_spacing_mm + 30.0)
    plate_h = 70.0
    t = a.wall_mm * 2.0

    lines: list[str] = []
    lines.append(_header(f"linear_axis_motor_mount name={a.name}"))
    lines.append(f"plate_w = {plate_w};")
    lines.append(f"plate_h = {plate_h};")
    lines.append(f"t = {t};")
    lines.append(f"hole_spacing = {hole_spacing};")
    lines.append(f"bore_d = {bore_d};")
    lines.append(f"mount_hole_d = 3.4;")
    lines.append("")
    lines.append("difference() {")
    lines.append("  cube([plate_w, plate_h, t], center=false);")
    lines.append("  translate([plate_w/2, plate_h/2, -1]) cylinder(h=t+2, d=bore_d, center=false);")
    for sx in (-1, 1):
        for sy in (-1, 1):
            lines.append(f"  translate([plate_w/2 + {sx}*hole_spacing/2, plate_h/2 + {sy}*hole_spacing/2, -1]) cylinder(h=t+2, d=mount_hole_d, center=false);")
    # Rod clamp through-holes (2)
    clamp_d = a.rod_d_mm + 2.0
    y = 12.0
    lines.append(f"  // rod clearances")
    lines.append(f"  translate([plate_w/2 - {a.rod_spacing_mm}/2, {y}, -1]) cylinder(h=t+2, d={clamp_d}, center=false);")
    lines.append(f"  translate([plate_w/2 + {a.rod_spacing_mm}/2, {y}, -1]) cylinder(h=t+2, d={clamp_d}, center=false);")
    lines.append("}")
    return "\n".join(lines) + "\n"


